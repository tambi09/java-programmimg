package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args) {
        String brand = "Chopard";
        String color = "Purple";
        double price = 4600.0;
        boolean waterResistant = true;
        boolean isSmart = false;
        char gender = 'F';

        System.out.println("The brand is    :\t\t" + brand );
        System.out.println("The color is    :\t\t" +color);
        System.out.println("The price is    :\t\t" +price);
        System.out.println("water resistant :\t\t"+waterResistant);
        System.out.println("Smart           :\t\t" + isSmart);
        System.out.println("Gender          :\t\t"+ gender);


    }
}
